def GetLimitTime():
    cptime = CpTimeChecker(1).checkRemainTime()
    if cptime[1] == 1 and cptime[0] != 0:
        while True:
            if CpTimeChecker(1).checkRemainTime()[0] == 0:
                time.sleep(0.5)
                break
    return True


