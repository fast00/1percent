    objStockChart = win32com.client.Dispatch("CpSysDib.StockChart")
    objStockChart.SetInputValue(0, str(code))  # 종목 코드
    objStockChart.SetInputValue(1, ord('1'))  # 개수로 조회
    objStockChart.SetInputValue(2, period)  # 최근 100일 치
    objStockChart.SetInputValue(3, period)  # 최근 100일 치
    objStockChart.SetInputValue(5, [6, 37])  # [0, 2, 3, 4, 5, 8]날짜,시가,고가,저가,종가,거래량
